#include "../header/libft.h"
#include <stdio.h>
void f(void *content){

	printf(" %s\n", content);
	// free(content);
}

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
    while (lst)
    {
        f(lst->content);
        lst = lst->next;
    }
}